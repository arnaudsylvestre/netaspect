         [Test]
         public void CheckSubscribeEventWithFilePathAndNoDebuggingInformation()
         {
          DoUnit.Test(new ClassAndAspectAndCallAcceptanceTestBuilder())
                 .ByDefiningAssembly(simpleClassAndWeaver =>
                    {
                       simpleClassAndWeaver.Aspect.AddAfterFieldAccess()
                          .WithParameter<int>("filePath");
                    })
                    .EnsureErrorHandler(e => e.Warnings.Add("The parameter filePath in method AfterSubscribeEventValue of type A.MyAspectAttribute will have the default value because there is no debugging information"))
                  .AndEnsureAssembly((assembly, actual) =>
                      {
                          var caller = actual.CreateCallerObject();
                          actual.CallCallerMethod(caller);
                          Assert.AreEqual(0, actual.Aspect.AfterSubscribeEventValueFilePath);
                      })
                  .AndLaunchTest();
         }

