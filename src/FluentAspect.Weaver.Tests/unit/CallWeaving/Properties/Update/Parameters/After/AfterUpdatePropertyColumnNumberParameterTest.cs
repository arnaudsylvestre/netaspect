    [TestFixture]
    public class AfterUpdatePropertyColumnNumberParameterTest 
    {
         [Test]
         public void CheckUpdatePropertyWithColumnNumberAndNoDebuggingInformation()
         {
          DoUnit.Test(new ClassAndAspectAndCallAcceptanceTestBuilder())
                 .ByDefiningAssembly(simpleClassAndWeaver =>
                    {
                       simpleClassAndWeaver.Aspect.AddAfterFieldAccess()
                          .WithParameter<int>("columnNumber");
                    })
                    .EnsureErrorHandler(e => e.Warnings.Add("The parameter columnNumber in method AfterUpdatePropertyValue of type A.MyAspectAttribute will have the default value because there is no debugging information"))
                  .AndEnsureAssembly((assembly, actual) =>
                      {
                          var caller = actual.CreateCallerObject();
                          actual.CallCallerMethod(caller);
                          Assert.AreEqual(0, actual.Aspect.AfterUpdatePropertyValueColumnNumber);
                      })
                  .AndLaunchTest();
         }

